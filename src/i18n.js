export function t(language, key, values = {}) {
  const template = MESSAGES[language]?.[key] || MESSAGES.zh[key] || key;
  return Object.entries(values).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    template
  );
}

export function applyStaticTranslations(root, language) {
  root.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(language, node.dataset.i18n);
  });

  root.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.setAttribute("placeholder", t(language, node.dataset.i18nPlaceholder));
  });

  root.querySelectorAll("[data-i18n-title]").forEach((node) => {
    node.setAttribute("title", t(language, node.dataset.i18nTitle));
  });

  root.querySelectorAll("[data-i18n-alt]").forEach((node) => {
    node.setAttribute("alt", t(language, node.dataset.i18nAlt));
  });
}
